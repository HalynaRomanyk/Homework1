package com.cursor;

public class Main {

    public static void main(String[] args) {
        double x, y, w, a, b, e, X;
        a = 1;
        b = 2;
        e = 3;
        x = -5;
        y = 0;
        w = -10;
        X = 100;

        System.out.println(x + y - a / 2 * (x + w));
        System.out.println(a * (x + x) / y - a * 3);
        System.out.println(a + b / x - y);
        System.out.println(X - e / 2 % e + 1);
    }
}

